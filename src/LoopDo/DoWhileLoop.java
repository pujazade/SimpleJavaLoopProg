package LoopDo;

public class DoWhileLoop {

	public static void main(String[] args) {
		
		int i= 1;
		do
		{
			System.out.println("Value of c =" +i);
			i++;                                                                   //11       
		}
		while(i<=10);                                                              //   11<=10  
        System.out.println();
        
	
		
        int a =20;
		do
		{
			System.out.println("Value of A ="+a);
			a--;	
		}
		while (a>=1);
        System.out.println();
        
       
        
       
        int b =50;
        do 
        {
        	System.out.print("Num="+b +",");
        	b++;
        }
        while (b<=70);
       }
    }
