package LoopTypeProg1;

public class ForLoop4 {

	public static void main(String[] args) {

		for ( int num=1;num<=10;num=num+1)           //num++    or    num=num+1
		{
			System.out.println(num);                //num =1  ... 1<=10   true condition
		}                                           //num =11 ... 11<=10   false condition
	
	}

}
