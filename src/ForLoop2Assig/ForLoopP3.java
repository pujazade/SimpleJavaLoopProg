package ForLoop2Assig;
//  EVEN PROGRAM FOR LOOP
public class ForLoopP3 {

	public static void main(String[] args) {
//print Even number from 2-10

		for (int i=2;i<=10;i=i+2)		
		{
			System.out.println("E.number =" +i);
		}

// WAP TO PRINT EVEN NUMBERS FROM 100 TO 2
		for (int i=100 ; i>=2 ; i=i-2)
		{
		    System.out.println("EVEN NUMBERS =" +i);
					
		}
			
	}

}
