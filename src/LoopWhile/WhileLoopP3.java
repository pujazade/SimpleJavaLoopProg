package LoopWhile;

public class WhileLoopP3 {

	public static void main(String[] args) {
//PRINT EVEN NUMBERS FROM 2-10		
				int c =2;
				while (c<=10)
				{
					System.out.print(c+ ",");
				    c=c+2;
				}
				{
					System.out.println();
					System.out.println();
		         }
//PRINT ODD NUMBERS FROM 5-51
				int D =5;
				while (D<=51)
				{
					System.out.print(D+ ",");
				    D=D+2;
				}
				{
					System.out.println();
					System.out.println();
		         }
//PRINT EVEN NUMBERS FROM 100-1
				int G =100;
				while (G >=1)
				{
					System.out.println(G+" ");
					G=G-2;
			    }
	     }

}
